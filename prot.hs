translate :: (Char, Char, Char) -> Char
translate (x, y, z) = case (x, y, z) of
	('A', 'A', 'A') -> 'K'
	('A', 'A', 'C') -> 'N'
	('A', 'A', 'G') -> 'K'
	('A', 'A', 'U') -> 'N'
	('A', 'C', 'A') -> 'T'
	('A', 'C', 'C') -> 'T'
	('A', 'C', 'G') -> 'T'
	('A', 'C', 'U') -> 'T'
	('A', 'G', 'A') -> 'R'
	('A', 'G', 'C') -> 'S'
	('A', 'G', 'G') -> 'R'
	('A', 'G', 'U') -> 'S'
	('A', 'U', 'A') -> 'I'
	('A', 'U', 'C') -> 'I'
	('A', 'U', 'G') -> 'M'
	('A', 'U', 'U') -> 'I'
	('C', 'A', 'A') -> 'Q'
	('C', 'A', 'C') -> 'H'
	('C', 'A', 'G') -> 'Q'
	('C', 'A', 'U') -> 'H'
	('C', 'C', 'A') -> 'P'
	('C', 'C', 'C') -> 'P'
	('C', 'C', 'G') -> 'P'
	('C', 'C', 'U') -> 'P'
	('C', 'G', 'A') -> 'R'
	('C', 'G', 'C') -> 'R'
	('C', 'G', 'G') -> 'R'
	('C', 'G', 'U') -> 'R'
	('C', 'U', 'A') -> 'L'
	('C', 'U', 'C') -> 'L'
	('C', 'U', 'G') -> 'L'
	('C', 'U', 'U') -> 'L'
	('G', 'A', 'A') -> 'E'
	('G', 'A', 'C') -> 'D'
	('G', 'A', 'G') -> 'E'
	('G', 'A', 'U') -> 'D'
	('G', 'C', 'A') -> 'A'
	('G', 'C', 'C') -> 'A'
	('G', 'C', 'G') -> 'A'
	('G', 'C', 'U') -> 'A'
	('G', 'G', 'A') -> 'G'
	('G', 'G', 'C') -> 'G'
	('G', 'G', 'G') -> 'G'
	('G', 'G', 'U') -> 'G'
	('G', 'U', 'A') -> 'V'
	('G', 'U', 'C') -> 'V'
	('G', 'U', 'G') -> 'V'
	('G', 'U', 'U') -> 'V'
	('U', 'A', 'A') -> '\n'
	('U', 'A', 'C') -> 'Y'
	('U', 'A', 'G') -> '\n'
	('U', 'A', 'U') -> 'Y'
	('U', 'C', 'A') -> 'S'
	('U', 'C', 'C') -> 'S'
	('U', 'C', 'G') -> 'S'
	('U', 'C', 'U') -> 'S'
	('U', 'G', 'A') -> '\n'
	('U', 'G', 'C') -> 'C'
	('U', 'G', 'G') -> 'W'
	('U', 'G', 'U') -> 'C'
	('U', 'U', 'A') -> 'L'
	('U', 'U', 'C') -> 'F'
	('U', 'U', 'G') -> 'L'
	('U', 'U', 'U') -> 'F'

codons :: [a] -> [(a, a, a)]
codons [] = []
codons (x:y:z:zs) = (x, y, z) : codons zs

main :: IO ()
main = do
	s <- getLine
	putStr . map translate . codons $ s
